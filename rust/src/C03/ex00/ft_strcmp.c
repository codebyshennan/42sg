int	ft_strcmp(char *s1, char *s2)
{
	int	count;
	int	diff;

	count = 0;
	diff = -1;
	while (s1[count] != '\0' || s2[count] != '\0')
	{
		diff = s1[count] - s2[count];
		if (diff != 0)
		{
			return (diff);
		}
		count++;
	}
	return (0);
}
